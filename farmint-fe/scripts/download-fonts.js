const downloadFont = (url, filepath) => {
  return new Promise((resolve, reject) => {
    https.get(url, (response) => {
      if (response.statusCode !== 200) {
        reject(new Error(`Failed to download ${url}: ${response.statusCode}`));
        return;
      }

      const file = fs.createWriteStream(filepath);
      response.pipe(file);

      file.on('finish', () => {
        file.close();
        resolve();
      });

      file.on('error', (err) => {
        fs.unlink(filepath, () => {});
        reject(err);
      });
    }).on('error', (err) => {
      reject(err);
    });
  });
};

const downloadAllFonts = async () => {
  const fontsDir = path.join(__dirname, '../public/fonts');

  for (const [fontFamily, weights] of Object.entries(fonts)) {
    for (const [weight, url] of Object.entries(weights)) {
      const filename = `${fontFamily}-${weight}.woff2`;
      const filepath = path.join(fontsDir, filename);
      
      console.log(`Downloading ${filename}...`);
      try {
        await downloadFont(url, filepath);
        console.log(`Downloaded ${filename}`);
      } catch (error) {
        console.error(`Error downloading ${filename}:`, error);
      }
    }
  }
};

downloadAllFonts().catch(console.error); 